class Vertex:
    def __init__(self, x, y, z, r, g, b, n0, n1, n2, u, v):
        self.x = x
        self.y = y
        self.z = z
        self.r = r
        self.g = g
        self.b = b
        self.n0 = n0  # Normal x component
        self.n1 = n1  # Normal y component
        self.n2 = n2  # Normal z component
        self.u = u
        self.v = v
